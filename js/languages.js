$(document).ready(function() {
  // load, on DOMContentLoaded
  var lang = localStorage.getItem('lang') || 'pl';
  $(".lang").each(function(index, element) {
    $(this).text(arrLang[lang][$(this).attr("data-key")]);
  });
  $(".langin").each(function(index, element) {
    $(this).attr("placeholder",arrLang[lang][$(this).attr("data-key")]);
  });

  $(".title").each(function(index, element) {
    title = arrLang[lang][$(this).attr("data-key")];
  });

  document.title = 'Kacper Budny | ' + title;
});

// get/set the selected language
$(".translate").click(function() {
  var lang = $(this).attr("id");

  var title;

  $(".lang").each(function(index, element) {
    $(this).text(arrLang[lang][$(this).attr("data-key")]);
  });

  $(".langin").each(function(index, element) {
    $(this).attr("placeholder",arrLang[lang][$(this).attr("data-key")]);
  });

  $(".title").each(function(index, element) {
    title = arrLang[lang][$(this).attr("data-key")];
  });

  document.title = 'Kacper Budny | ' + title;
  // save, in your .translate click handler
  localStorage.setItem('lang', $(this).attr('id'));
});
